    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int n1=Integer.parseInt(Text_1.getText());
        int n2=Integer.parseInt(Text_2.getText());
        int n3=Integer.parseInt(Text_3.getText());
        
        DefaultCategoryDataset datos = new DefaultCategoryDataset();
        datos.setValue(n1, "Matematicas", "Camilo");
        datos.setValue(n2, "Matematicas", "David");
        datos.setValue(n3, "Matematicas", "Samuel");
        JFreeChart grafico_barras = ChartFactory.createBarChart3D(
                "Calificaciones de Matematicas",
                "Estudiantes de Matematicas",
                "Calificaci√≥n",
                datos,
                PlotOrientation.VERTICAL,
                true,
                true, 
                false             
        );
        ChartPanel panel = new ChartPanel(grafico_barras);
        panel.setMouseWheelEnabled(true);
        panel.setPreferredSize(new Dimension(400,200));
        Panel1.setLayout(new BorderLayout());
        Panel1.add(panel,BorderLayout.NORTH);
        
        pack();
        repaint();
        
    }//GEN-LAST:event_jButton1ActionPerformed

